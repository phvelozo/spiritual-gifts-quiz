def get_translations(lang="pt"):
    """
    Get all translations for a specific language.
    
    Args:
        lang: Language code ("pt", "es", or "en")
    
    Returns:
        dict: Dictionary with 'questions', 'gift_names', and 'ui' keys
    """
    if lang not in LANGUAGES:
        lang = "pt"  # Default to Portuguese
    
    return {
        "questions": QUESTIONS.get(lang, QUESTIONS["pt"]),
        "gift_names": GIFT_NAMES.get(lang, GIFT_NAMES["pt"]),
        "ui": UI_STRINGS.get(lang, UI_STRINGS["pt"]),
    }


def get_ui_string(key, lang="pt"):
    """Get a UI string for a specific language."""
    return UI_STRINGS.get(lang, UI_STRINGS["pt"]).get(key, key)


def get_question(num, lang="pt"):
    """Get a question for a specific language."""
    return QUESTIONS.get(lang, QUESTIONS["pt"]).get(num, "")


def get_gift_name(gift, lang="pt"):
    """Get a gift name for a specific language."""
    return GIFT_NAMES.get(lang, GIFT_NAMES["pt"]).get(gift, gift)

